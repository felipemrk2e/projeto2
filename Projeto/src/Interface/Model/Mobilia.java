
package Interface.Model;

public class Mobilia {
    
    private int idMobilia;
    private String tipoMobilia;
    private Imovel imovel;
        
    public int getIdMobilia() {
        return idMobilia;
    }

    public void setIdMobilia(int idMobilia) {
        this.idMobilia = idMobilia;
    }

    public String getTipoMobilia() {
        return tipoMobilia;
    }

    public void setTipoMobilia(String tipoMobilia) {
        this.tipoMobilia = tipoMobilia;
    }

    public Imovel getImovel() {
        return imovel;
    }

    public void setImovel(Imovel imovel) {
        this.imovel = imovel;
    }
    
    

}
